let currentPolicyTab = 'terms';

function openPolicy(tab) {
  currentPolicyTab = tab || 'terms';
  switchPolicyTab(currentPolicyTab);
  document.getElementById('policy-overlay').classList.add('on');
}

function closePolicy() {
  document.getElementById('policy-overlay').classList.remove('on');
}

function switchPolicyTab(tab) {
  currentPolicyTab = tab;
  // Update tabs
  ['terms','privacy','rules'].forEach(t => {
    document.getElementById('ptab-' + t)?.classList.toggle('active', t === tab);
  });
  // Update content
  const body = document.getElementById('policy-body');
  if (body) {
    body.innerHTML = POLICY_CONTENT[tab] || '';
    body.scrollTop = 0;
  }
}

// Close on overlay click
document.getElementById('policy-overlay')?.addEventListener('click', e => {
  if (e.target === document.getElementById('policy-overlay')) closePolicy();
});

