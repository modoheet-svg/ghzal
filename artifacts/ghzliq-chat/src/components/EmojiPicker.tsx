interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  onClose: () => void;
}

export default function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState("recent");
  const [search, setSearch] = useState("");
  const pickerRef = useRef<HTMLDivElement>(null);

  useEffect(() => {
    function handleClick(e: MouseEvent) {
      if (pickerRef.current && !pickerRef.current.contains(e.target as Node)) {
        onClose();
      }
    }
    document.addEventListener("mousedown", handleClick);
    return () => document.removeEventListener("mousedown", handleClick);
  }, [onClose]);

  const currentCategory = EMOJI_CATEGORIES.find((c) => c.id === activeCategory);

  const filteredEmojis = search
    ? EMOJI_CATEGORIES.flatMap((c) => c.emojis).filter((_, i) => {
        return true;
      }).slice(0, 80)
    : currentCategory?.emojis ?? [];

  return (
    <div
      ref={pickerRef}
      className="absolute bottom-16 right-0 w-80 rounded-2xl shadow-2xl z-50 overflow-hidden"
      style={{
        background: "hsl(234 48% 7%)",
        border: "1px solid hsl(234 35% 18%)",
        boxShadow: "0 -4px 24px rgba(0,0,0,0.5), 0 0 40px rgba(245,158,11,0.08)",
      }}
    >
      <div className="p-3 border-b" style={{ borderColor: "hsl(234 35% 18%)" }}>
        <div className="relative">
          <Search
            className="absolute right-3 top-1/2 -translate-y-1/2 text-muted-foreground"
            size={14}
          />
          <input
            type="text"
            placeholder="ابحث عن إيموجي..."
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            className="w-full pr-9 pl-3 py-2 rounded-xl text-sm outline-none"
            style={{
              background: "hsl(234 42% 10%)",
              border: "1px solid hsl(234 35% 18%)",
              color: "hsl(45 85% 88%)",
              direction: "rtl",
            }}
          />
        </div>
      </div>

      <div
        className="flex overflow-x-auto py-2 px-2 gap-1"
        style={{ borderBottom: "1px solid hsl(234 35% 18%)" }}
      >
        {EMOJI_CATEGORIES.map((cat) => (
          <button
            key={cat.id}
            onClick={() => { setActiveCategory(cat.id); setSearch(""); }}
            title={cat.label}
            className="flex-shrink-0 w-9 h-9 flex items-center justify-center rounded-lg text-lg transition-all"
            style={{
              background:
                activeCategory === cat.id
                  ? "hsl(38 92% 50% / 0.2)"
                  : "transparent",
              border:
                activeCategory === cat.id
                  ? "1px solid hsl(38 92% 50% / 0.5)"
                  : "1px solid transparent",
            }}
          >
            {cat.icon}
          </button>
        ))}
      </div>

      <div
        className="p-2 overflow-y-auto"
        style={{ maxHeight: "220px" }}
      >
        {!search && currentCategory && (
          <p className="text-xs mb-2 px-1" style={{ color: "hsl(234 20% 58%)" }}>
            {currentCategory.label}
          </p>
        )}
        <div className="emoji-picker-grid">
          {filteredEmojis.map((emoji, i) => (
            <button
              key={i}
              className="emoji-btn"
              onClick={() => onSelect(emoji)}
              data-testid={`emoji-btn-${i}`}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}
